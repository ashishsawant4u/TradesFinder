package com.trades.demo.models;

public class LineChartModel 
{
	private String time;
	
	private double openInterst;

	
	private double price;


	public String getTime() {
		return time;
	}


	public void setTime(String time) {
		this.time = time;
	}


	public double getOpenInterst() {
		return openInterst;
	}


	public void setOpenInterst(double openInterst) {
		this.openInterst = openInterst;
	}


	public double getPrice() {
		return price;
	}


	public void setPrice(double price) {
		this.price = price;
	}
	
	
	
}
