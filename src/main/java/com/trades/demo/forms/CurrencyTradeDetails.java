package com.trades.demo.forms;

public class CurrencyTradeDetails 
{
	private String storyAndViewBuilding;
	
	private String tradeBias;
	
	private String opportunity;
	
	private String whatIfAnalysis;
	
	private String chartImageUrl;
	
	private CurrencyCalculatorForm calculations;
	
	private String confirmation;
	
	private boolean executed;
	
	private String outcome;
	
	private String comment;
	
	private String learnings;
	
	private String tradeState;

	public String getStoryAndViewBuilding() {
		return storyAndViewBuilding;
	}

	public void setStoryAndViewBuilding(String storyAndViewBuilding) {
		this.storyAndViewBuilding = storyAndViewBuilding;
	}

	public String getTradeBias() {
		return tradeBias;
	}

	public void setTradeBias(String tradeBias) {
		this.tradeBias = tradeBias;
	}

	public String getOpportunity() {
		return opportunity;
	}

	public void setOpportunity(String opportunity) {
		this.opportunity = opportunity;
	}

	public String getWhatIfAnalysis() {
		return whatIfAnalysis;
	}

	public void setWhatIfAnalysis(String whatIfAnalysis) {
		this.whatIfAnalysis = whatIfAnalysis;
	}

	public String getChartImageUrl() {
		return chartImageUrl;
	}

	public void setChartImageUrl(String chartImageUrl) {
		this.chartImageUrl = chartImageUrl;
	}

	public CurrencyCalculatorForm getCalculations() {
		return calculations;
	}

	public void setCalculations(CurrencyCalculatorForm calculations) {
		this.calculations = calculations;
	}

	public String getConfirmation() {
		return confirmation;
	}

	public void setConfirmation(String confirmation) {
		this.confirmation = confirmation;
	}

	public boolean isExecuted() {
		return executed;
	}

	public void setExecuted(boolean executed) {
		this.executed = executed;
	}

	public String getOutcome() {
		return outcome;
	}

	public void setOutcome(String outcome) {
		this.outcome = outcome;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getLearnings() {
		return learnings;
	}

	public void setLearnings(String learnings) {
		this.learnings = learnings;
	}

	public String getTradeState() {
		return tradeState;
	}

	public void setTradeState(String tradeState) {
		this.tradeState = tradeState;
	}
	
}
